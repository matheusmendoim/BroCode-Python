#strings: series of characters

first_name = "Matheus"
last_name = "Mendoim"
full_name = first_name + " " + last_name

print(type(first_name))
print("Hello", full_name)


#integers: contains whole integers numbers

age = 21
age += 1

print(age)
print(type(age))
print("Your age is: "+str(age))


#float: data type that stores decimal numbers

height = 175.03

print(height)
print(type(height))
print("Your height is "+str(height)+" cm")


#boolean: variable that only store true of false

human = True

print(human)
print(type(human))
print("Are you a human? "+str(human))


